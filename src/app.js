let elements = galleryItems.map(elem => {
  let elementLi = document.createElement('li')
  elementLi.classList.add('gallery__item')
  
  let elementA = document.createElement('a')
  elementA.classList.add('gallery__link')
  elementLi.appendChild(elementA)
  
  let elementImg = document.createElement('img')
  elementImg.classList.add('gallery__image')
  elementImg.src = elem.preview
  elementImg.dataset.source = elem.original
  elementImg.alt = elem.description

  elementA.appendChild(elementImg)
  return elementLi
})

document.querySelector('.js-gallery').append(...elements)

document.addEventListener('click', e => {
  if (e.target.classList.contains('gallery__image')){
    document.querySelector('div.lightbox .lightbox__image').src = e.target.dataset.source
    document.querySelector('div.lightbox .lightbox__image').alt = e.target.alt
    document.querySelector('div.lightbox').classList.add('is-open')
  }
})

document.querySelector('button[data-action="close-lightbox"]').addEventListener("click", e => {
  document.querySelector('div.lightbox .lightbox__image').src = ''
  document.querySelector('div.lightbox .lightbox__image').alt = ''
  document.querySelector('div.lightbox').classList.remove('is-open')
})